
# Programa para comparar dos números enteros

# Solicita los dos números al usuario
numero1 = int(input("Ingrese el primer número entero: "))
numero2 = int(input("Ingrese el segundo número entero: "))

# Compara si el primero es mayor que el segundo
resultado = numero1 > numero2

# Muestra el resultado (True o False)
print("¿El primer número es mayor que el segundo?:", resultado)
